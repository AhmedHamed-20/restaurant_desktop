class LoginModel {
  String status;
  String token;
  Data data;
  LoginModel.fromjson(Map<String, dynamic> json) {
    status = json['status'];
    token = json['token'];
    data = json['data'] != null ? Data.fromjson(json['data']) : null;
  }
}

class Data {
  UserData user;
  Data.fromjson(Map<String, dynamic> json) {
    user = json['user'] != null ? UserData.fromjson(json['user']) : null;
  }
}

class UserData {
  String photo;
  String role;
  String id;
  String name;
  String email;
  UserData.fromjson(Map<String, dynamic> json) {
    photo = json['photo'];
    role = json['role'];
    id = json['_id'];
    name = json['name'];
    email = json['email'];
  }
}
